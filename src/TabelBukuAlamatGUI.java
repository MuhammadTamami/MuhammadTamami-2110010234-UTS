    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
   
        //Mengambil inputan dari textfield 
        String nama = tfNama.getText();
        String alamat = tfAlamat.getText();
        String relasi = tfRelasi.getText();
        String telepon = tfTelepon.getText();
        
        //Validasi apabila salah satu inputan ada yang kosong
        if (nama.isEmpty() || alamat.isEmpty() || relasi.isEmpty() || telepon.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Mohon untuk massukan semua field",
                    "Coba lagi",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            //Mendapatkan model tabel
            DefaultTableModel model = (DefaultTableModel) tableBukuAlamat.getModel();
            //Menambahkan data ke setiap baris
            model.addRow(new Object[]{nama, alamat, relasi, telepon});
            
            //Setelah selesai menambahkan data, mengkosongkan inputan textfield
            tfNama.setText("");
            tfAlamat.setText("");
            tfRelasi.setText("");
            tfTelepon.setText("");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        //Mendapatkan baris yang ingin dihapus
        int row = tableBukuAlamat.getSelectedRow();
        
        //Memeriksa apakah ada baris yang dipilih
        if (row < 0){
            //Menampilkan pesan apabila tidak ada baris yang dipilih
            JOptionPane.showMessageDialog(this,
                    "Tidak ada baris yang dipilih, silahkan pilih baris yang dipilih",
                    "Pilih baris",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            //Mendapatkan model tabel
            DefaultTableModel model = (DefaultTableModel) tableBukuAlamat.getModel();
            //Menghapus baris yang dipilih dari tabel
            model.removeRow(row);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        // TODO add your handling code here:
        
        //Mendapatkan model tabel yang dipakai
        DefaultTableModel model = (DefaultTableModel) tableBukuAlamat.getModel();
        Vector<Vector> tableData = model.getDataVector();
        
        //menyimpan objek ke sebuah file
        try {
            //membuka file output stream dan output stream
            FileOutputStream file = new FileOutputStream("bukualamat.txt");
            ObjectOutputStream output = new ObjectOutputStream(file);
            
            //menulis objek (data yang ada di dalam tabel) ke dalam sebuah file
            output.writeObject(tableData);
            
            //menutup output stream setelah selesai menulis
            output.close();
            file.close();
        } catch(Exception ex){
            //menangani exception dan mencetak stack trace
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowClosing

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            //membuka file input stream dan object input stream
            FileInputStream file = new FileInputStream("bukualamat.txt");
            ObjectInputStream input = new ObjectInputStream(file);
            
            //membaca objek yang berisi data tabel
            Vector<Vector> tableData = (Vector<Vector>)input.readObject();
            
            //menutup input stream setelah membaca objek
            input.close();
            file.close();
            
            //mendapatkan model table
            DefaultTableModel model = (DefaultTableModel) tableBukuAlamat.getModel();
            
            //Iterasi melalui data dan tambahkan setiap baris ke model tabel
            for (int i = 0; i < tableData.size(); i++) {
                Vector row = tableData.get(i);
                model.addRow(new Object[]{row.get(0), row.get(1), row.get(2), row.get(3)});
            }
        } catch(Exception ex) {
            //menangani exception dan mencetak stack trace
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

