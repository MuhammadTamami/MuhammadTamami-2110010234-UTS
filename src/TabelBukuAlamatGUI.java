    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        // TODO add your handling code here:
        
        //Menentukan File yang ingin diexport ke Jtable
        String filePath = "C:\\Users\\Tama\\Documents\\NetBeansProjects\\BukuAlamat\\daftarbukualamat.txt";
        File file = new File(filePath);
        try {
            //Membuat filewriter dan bufferedwriter untuk file yang ingin diexport
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            
            //Iterasi untuk setiap baris dan kolom di Jtable
            for(int i = 0; i < tableBukuAlamat.getRowCount(); i++){//baris
                for(int j = 0; j < tableBukuAlamat.getColumnCount(); j++){//kolom
                    //menulis data ke dalam file, dipisahkan dengan baris
                    bw.write(tableBukuAlamat.getValueAt(i, j).toString()+" ");
                }
                //Setelah selesai menulis setiap baris, pindah ke baris baru
                bw.newLine();
            }
            //menutup bufferedwriter dan filewriter setelah selesai menulis
            bw.close();
            fw.close();
            
            //Menampilkan pesan sukses apabila export berhasil
            JOptionPane.showMessageDialog(null, "Berhasil export ke file", "Export Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            //Menampilkan pesan gagal apabila export tidak berhasil
            Logger.getLogger(TabelBukuAlamatGUI.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Gagal export ke file", "Export Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExportActionPerformed

    private void btnImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportActionPerformed
        // TODO add your handling code here:
        
        //Menentukan file yang ingin diimport
        String filePath = "C:\\Users\\Tama\\Documents\\NetBeansProjects\\BukuAlamat\\tambahdaftarbukualamat.txt";   
        File file = new File(filePath);
        
        try {
            //Membuat filereader dan  bufferedreader untuk membaca file yang ingin diimport
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
            //DefaultTableModel untuk Jtable(Tujuan import)
            DefaultTableModel model = (DefaultTableModel)tableBukuAlamat.getModel();
            
            //Membaca baris dari file setelah itu mengkonversikan ke bentuk array
            Object[] lines = br.lines().toArray();
            
            //Iterasi melalui baris dan menambahkan data setiap baris ke Jtable
            for(int i=0; i < lines.length; i++){
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
            }
            
            //memunculkan pesan apabila berhasil import
            JOptionPane.showMessageDialog(null, "Berhasil mengimport file", "Import Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (FileNotFoundException ex) {
            //memunculkan pesan apabila gagal import
            Logger.getLogger(TabelBukuAlamatGUI.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(null, "Gagal mengimport file", "Import Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnImportActionPerformed

